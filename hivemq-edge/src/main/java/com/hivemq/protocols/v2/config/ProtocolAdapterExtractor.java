package com.hivemq.protocols.v2.config;

import com.hivemq.configuration.entity.HiveMQConfigEntity;
import com.hivemq.configuration.reader.Configurator;
import com.hivemq.configuration.reader.ReloadableExtractor;
import jakarta.xml.bind.ValidationEvent;
import jakarta.xml.bind.helpers.ValidationEventImpl;
import java.util.ArrayList;
import java.util.HashSet;
import java.util.List;
import java.util.Optional;
import java.util.Set;
import java.util.function.Consumer;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

/**
 * Reads the {@code <v2-protocol-adapters>} configuration section, mirroring the legacy
 * {@link com.hivemq.configuration.reader.ProtocolAdapterExtractor} consumer-callback pattern (parse on read, validate,
 * notify a registered consumer) but <b>read-only</b>: Edge never writes this section — it is generated by external
 * tooling. The registered consumer (wired in a later task) {@code tell}s a {@code ConfigurationChanged} to the
 * Protocol Adapter Manager mailbox; this is the same path at startup and on every reload.
 * <p>
 * Validation rejects a section (returns {@link Configurator.ConfigResult#ERROR}, no consumer notification) when an
 * adapter fails its own {@link ProtocolAdapterEntity#validate(List) checks} — duplicate tag names, mapping references
 * that do not resolve to a declared tag, an invalid {@code protocol-id}, or the watchdog/command-timeout invariant
 * (S32) — or when two adapters share an {@code adapter-id}. A bad section is reported with a clear
 * message and the previously-applied configuration is left untouched.
 */
public class ProtocolAdapterExtractor
        implements ReloadableExtractor<List<@NotNull ProtocolAdapterEntity>, List<@NotNull ProtocolAdapterEntity>> {

    private volatile @NotNull List<ProtocolAdapterEntity> allConfigs = List.of();
    private volatile @Nullable Consumer<List<@NotNull ProtocolAdapterEntity>> consumer =
            configs -> log.debug("No v2 protocol adapter consumer registered yet");

    public @NotNull List<ProtocolAdapterEntity> getAllConfigs() {
        return allConfigs;
    }

    public @NotNull Optional<ProtocolAdapterEntity> getAdapterByAdapterId(final @NotNull String adapterId) {
        return allConfigs.stream()
                .filter(adapter -> adapter.getAdapterId().equals(adapterId))
                .findFirst();
    }

    @Override
    public synchronized @NotNull Configurator.ConfigResult updateConfig(final @NotNull HiveMQConfigEntity config) {
        final List<ProtocolAdapterEntity> newConfigs = List.copyOf(config.getV2ProtocolAdapterConfig());
        final List<ValidationEvent> validationEvents = new ArrayList<>();
        newConfigs.forEach(entity -> entity.validate(validationEvents));
        validateUniqueAdapterIds(newConfigs, validationEvents);

        final List<ValidationEvent> errorEvents = validationEvents.stream()
                .filter(event -> event.getSeverity() == ValidationEvent.FATAL_ERROR
                        || event.getSeverity() == ValidationEvent.ERROR)
                .toList();
        if (!errorEvents.isEmpty()) {
            errorEvents.forEach(event -> log.error("v2 protocol adapter config error: {}", event.getMessage()));
            return Configurator.ConfigResult.ERROR;
        }

        allConfigs = newConfigs;
        notifyConsumer();
        return Configurator.ConfigResult.SUCCESS;
    }

    private static void validateUniqueAdapterIds(
            final @NotNull List<ProtocolAdapterEntity> configs, final @NotNull List<ValidationEvent> validationEvents) {
        final Set<String> seen = new HashSet<>();
        configs.stream().map(ProtocolAdapterEntity::getAdapterId).forEach(adapterId -> {
            if (!seen.add(adapterId)) {
                validationEvents.add(new ValidationEventImpl(
                        ValidationEvent.FATAL_ERROR, "duplicate v2 adapter-id [" + adapterId + "]", null));
            }
        });
    }

    private void notifyConsumer() {
        final Consumer<List<@NotNull ProtocolAdapterEntity>> currentConsumer = this.consumer;
        if (currentConsumer != null) {
            currentConsumer.accept(allConfigs);
        }
    }

    @Override
    public synchronized void registerConsumer(final @NotNull Consumer<List<@NotNull ProtocolAdapterEntity>> consumer) {
        this.consumer = consumer;
        notifyConsumer();
    }

    @Override
    public boolean needsRestartWithConfig(final @NotNull HiveMQConfigEntity config) {
        return false;
    }

    @Override
    public synchronized void sync(final @NotNull HiveMQConfigEntity entity) {
        // Round-trip what was loaded so a disk write triggered by another subsystem preserves the section verbatim.
        // Edge never modifies the v2 section; this only re-emits the bytes it read.
        entity.getV2ProtocolAdapterConfig().clear();
        entity.getV2ProtocolAdapterConfig().addAll(allConfigs);
    }
}
