package com.hivemq.protocols.v2.config;

import jakarta.xml.bind.annotation.XmlAccessType;
import jakarta.xml.bind.annotation.XmlAccessorType;
import jakarta.xml.bind.annotation.XmlElement;
import jakarta.xml.bind.annotation.XmlElementWrapper;
import jakarta.xml.bind.annotation.XmlType;
import java.util.ArrayList;
import java.util.List;
import java.util.Objects;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

/**
 * The {@code <v2>} configuration container — the side-by-side, disjoint counterpart of the legacy top-level sections.
 * It nests the {@code <protocol-adapters>} list (each entry a {@link ProtocolAdapterEntity}), mirroring the legacy
 * {@code <protocol-adapters><protocol-adapter>} shape but scoped under {@code <v2>}. Absent or empty ⇒ an empty
 * adapter list, so old configs parse unchanged. Edge only reads this container; it is generated by external tooling,
 * never written by Edge.
 */
@SuppressWarnings("FieldMayBeFinal")
@XmlAccessorType(XmlAccessType.NONE)
@XmlType(name = "v2ConfigEntity")
public class V2ConfigEntity {

    @XmlElementWrapper(name = "protocol-adapters")
    @XmlElement(name = "protocol-adapter")
    private @NotNull List<ProtocolAdapterEntity> protocolAdapters = new ArrayList<>();

    // no-arg constructor for JAXB; the field initializer carries the empty default
    public V2ConfigEntity() {}

    public V2ConfigEntity(final @NotNull List<ProtocolAdapterEntity> protocolAdapters) {
        this.protocolAdapters = protocolAdapters;
    }

    public @NotNull List<ProtocolAdapterEntity> getProtocolAdapters() {
        return protocolAdapters;
    }

    @Override
    public boolean equals(final @Nullable Object o) {
        if (this == o) {
            return true;
        }
        if (!(o instanceof final V2ConfigEntity that)) {
            return false;
        }
        return Objects.equals(protocolAdapters, that.protocolAdapters);
    }

    @Override
    public int hashCode() {
        return Objects.hash(protocolAdapters);
    }
}
