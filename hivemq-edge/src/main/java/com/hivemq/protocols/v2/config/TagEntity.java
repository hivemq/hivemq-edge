package com.hivemq.protocols.v2.config;

import com.hivemq.configuration.entity.EntityValidatable;
import jakarta.xml.bind.ValidationEvent;
import jakarta.xml.bind.annotation.XmlAccessType;
import jakarta.xml.bind.annotation.XmlAccessorType;
import jakarta.xml.bind.annotation.XmlElement;
import jakarta.xml.bind.annotation.XmlType;
import java.util.List;
import java.util.Objects;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

/**
 * A tag in the {@code <v2>} section — one node the adapter exposes, plus the persisted
 * per-aspect activation preferences that carry the tag lifecycle (D7). The {@code node-string} is the opaque
 * JSON node definition, stored as-is and round-tripped to the adapter unchanged. {@code read-activated} and
 * {@code write-activated} default to {@code true} when absent; a reload that flips only these flags is an
 * {@code ACTIVATION_ONLY} change applied without reconnecting. Edge only reads this section; it is generated by
 * external tooling.
 */
@SuppressWarnings({"FieldMayBeFinal", "unused"})
@XmlAccessorType(XmlAccessType.NONE)
// Distinct XML type name so the JAXB context can host this beside the identically-named legacy
// com.hivemq.configuration.entity.adapter.TagEntity without a type-name collision.
@XmlType(name = "v2TagEntity")
public class TagEntity implements EntityValidatable {

    private static final long DEFAULT_POLL_INTERVAL_MILLIS = 5000;

    @XmlElement(name = "name", required = true)
    private @NotNull String name = "";

    @XmlElement(name = "node-string", required = true)
    private @NotNull String nodeString = "";

    @XmlElement(name = "read-activated", defaultValue = "true")
    private boolean readActivated = true;

    @XmlElement(name = "write-activated", defaultValue = "true")
    private boolean writeActivated = true;

    @XmlElement(name = "pollable")
    private boolean pollable = false;

    @XmlElement(name = "subscribable")
    private boolean subscribable = false;

    @XmlElement(name = "poll-interval-millis")
    private long pollIntervalMillis = DEFAULT_POLL_INTERVAL_MILLIS;

    @XmlElement(name = "access")
    private @NotNull AccessFlagsEntity access = new AccessFlagsEntity();

    // no-arg constructor for JAXB; field initializers carry the defaults
    public TagEntity() {}

    public TagEntity(
            final @NotNull String name,
            final @NotNull String nodeString,
            final boolean readActivated,
            final boolean writeActivated,
            final boolean pollable,
            final boolean subscribable,
            final long pollIntervalMillis,
            final @NotNull AccessFlagsEntity access) {
        this.name = name;
        this.nodeString = nodeString;
        this.readActivated = readActivated;
        this.writeActivated = writeActivated;
        this.pollable = pollable;
        this.subscribable = subscribable;
        this.pollIntervalMillis = pollIntervalMillis;
        this.access = access;
    }

    public @NotNull String getName() {
        return name;
    }

    public @NotNull String getNodeString() {
        return nodeString;
    }

    public boolean isReadActivated() {
        return readActivated;
    }

    public boolean isWriteActivated() {
        return writeActivated;
    }

    public boolean isPollable() {
        return pollable;
    }

    public boolean isSubscribable() {
        return subscribable;
    }

    public long getPollIntervalMillis() {
        return pollIntervalMillis;
    }

    public @NotNull AccessFlagsEntity getAccess() {
        return access;
    }

    @Override
    public void validate(final @NotNull List<ValidationEvent> validationEvents) {
        EntityValidatable.notEmpty(validationEvents, name, "tag name");
        EntityValidatable.notEmpty(validationEvents, nodeString, "tag [" + name + "] node-string");
        EntityValidatable.notMatch(
                validationEvents,
                () -> pollIntervalMillis > 0,
                () -> "tag [" + name + "] poll-interval-millis (" + pollIntervalMillis + ") must be positive");
    }

    @Override
    public boolean equals(final @Nullable Object o) {
        if (o instanceof final TagEntity that) {
            return readActivated == that.readActivated
                    && writeActivated == that.writeActivated
                    && pollable == that.pollable
                    && subscribable == that.subscribable
                    && pollIntervalMillis == that.pollIntervalMillis
                    && Objects.equals(name, that.name)
                    && Objects.equals(nodeString, that.nodeString)
                    && Objects.equals(access, that.access);
        }
        return false;
    }

    @Override
    public int hashCode() {
        return Objects.hash(
                name, nodeString, readActivated, writeActivated, pollable, subscribable, pollIntervalMillis, access);
    }
}
