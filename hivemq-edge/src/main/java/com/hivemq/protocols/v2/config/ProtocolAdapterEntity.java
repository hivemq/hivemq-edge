package com.hivemq.protocols.v2.config;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.hivemq.configuration.entity.EntityValidatable;
import com.hivemq.configuration.reader.ArbitraryValuesMapAdapter;
import jakarta.xml.bind.ValidationEvent;
import jakarta.xml.bind.annotation.XmlAccessType;
import jakarta.xml.bind.annotation.XmlAccessorType;
import jakarta.xml.bind.annotation.XmlElement;
import jakarta.xml.bind.annotation.XmlElementWrapper;
import jakarta.xml.bind.annotation.XmlType;
import jakarta.xml.bind.annotation.adapters.XmlJavaTypeAdapter;
import jakarta.xml.bind.helpers.ValidationEventImpl;
import java.util.ArrayList;
import java.util.HashMap;
import java.util.HashSet;
import java.util.LinkedHashSet;
import java.util.List;
import java.util.Map;
import java.util.Objects;
import java.util.Set;
import java.util.regex.Pattern;
import java.util.stream.Collectors;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

/**
 * One v2 adapter instance in the {@code <v2-protocol-adapters>} section — the disjoint, side-by-side
 * counterpart of the legacy {@link com.hivemq.configuration.entity.adapter.ProtocolAdapterEntity} (per N1, the same
 * name distinguished by package). It carries the adapter identity, the config-declared direction activation goals, the
 * opaque adapter configuration, the retry / watchdog / command-timeout policy, the tag set with its persisted
 * per-aspect activation preferences (D7), and the northbound / southbound mappings that drive {@code used}
 * derivation. Edge only reads this section; it is generated by external tooling, never written by Edge.
 */
@SuppressWarnings({"FieldMayBeFinal", "unused"})
@XmlAccessorType(XmlAccessType.NONE)
// Distinct XML type name so the JAXB context can host this beside the identically-named legacy
// com.hivemq.configuration.entity.adapter.ProtocolAdapterEntity without a type-name collision.
@XmlType(name = "v2ProtocolAdapterEntity")
public class ProtocolAdapterEntity implements EntityValidatable {

    /** v2 config version — {@code >= 2} marks a v2 type; the authoritative discriminator stays the section. */
    public static final int DEFAULT_CONFIG_VERSION = 2;

    public static final long DEFAULT_WATCHDOG_TIMEOUT_MILLIS = 30_000;
    public static final long DEFAULT_COMMAND_TIMEOUT_MILLIS = 10_000;

    private static final @NotNull Pattern PROTOCOL_ID_PATTERN = Pattern.compile("[A-Za-z0-9][A-Za-z0-9._-]*");

    @XmlElement(name = "adapter-id", required = true)
    private @NotNull String adapterId = "";

    @XmlElement(name = "protocol-id", required = true)
    private @NotNull String protocolId = "";

    @XmlElement(name = "config-version", defaultValue = "" + DEFAULT_CONFIG_VERSION)
    private int configVersion = DEFAULT_CONFIG_VERSION;

    @XmlElement(name = "northbound-activated", defaultValue = "true")
    private boolean northboundActivated = true;

    @XmlElement(name = "southbound-activated", defaultValue = "true")
    private boolean southboundActivated = true;

    @XmlElement(name = "skip-verification", defaultValue = "false")
    private boolean skipVerification = false;

    @XmlElement(name = "adapter-configuration")
    @XmlJavaTypeAdapter(ArbitraryValuesMapAdapter.class)
    private @NotNull Map<String, Object> adapterConfiguration = new HashMap<>();

    @XmlElement(name = "retry-policy")
    private @NotNull RetryPolicyEntity retryPolicy = new RetryPolicyEntity();

    @XmlElement(name = "watchdog-timeout-millis")
    private long watchdogTimeoutMillis = DEFAULT_WATCHDOG_TIMEOUT_MILLIS;

    @XmlElement(name = "command-timeout-millis")
    private long commandTimeoutMillis = DEFAULT_COMMAND_TIMEOUT_MILLIS;

    @XmlElementWrapper(name = "tags")
    @XmlElement(name = "tag")
    private @NotNull List<TagEntity> tags = new ArrayList<>();

    @XmlElementWrapper(name = "northbound-mappings")
    @XmlElement(name = "northbound-mapping")
    private @NotNull List<NorthboundMappingEntity> northboundMappings = new ArrayList<>();

    @XmlElementWrapper(name = "southbound-mappings")
    @XmlElement(name = "southbound-mapping")
    private @NotNull List<SouthboundMappingEntity> southboundMappings = new ArrayList<>();

    // no-arg constructor for JAXB; field initializers carry the defaults
    public ProtocolAdapterEntity() {}

    public ProtocolAdapterEntity(
            final @NotNull String adapterId,
            final @NotNull String protocolId,
            final int configVersion,
            final boolean northboundActivated,
            final boolean southboundActivated,
            final boolean skipVerification,
            final @NotNull Map<String, Object> adapterConfiguration,
            final @NotNull RetryPolicyEntity retryPolicy,
            final long watchdogTimeoutMillis,
            final long commandTimeoutMillis,
            final @NotNull List<TagEntity> tags,
            final @NotNull List<NorthboundMappingEntity> northboundMappings,
            final @NotNull List<SouthboundMappingEntity> southboundMappings) {
        this.adapterId = adapterId;
        this.protocolId = protocolId;
        this.configVersion = configVersion;
        this.northboundActivated = northboundActivated;
        this.southboundActivated = southboundActivated;
        this.skipVerification = skipVerification;
        this.adapterConfiguration = adapterConfiguration;
        this.retryPolicy = retryPolicy;
        this.watchdogTimeoutMillis = watchdogTimeoutMillis;
        this.commandTimeoutMillis = commandTimeoutMillis;
        this.tags = tags;
        this.northboundMappings = northboundMappings;
        this.southboundMappings = southboundMappings;
    }

    public @NotNull String getAdapterId() {
        return adapterId;
    }

    public @NotNull String getProtocolId() {
        return protocolId;
    }

    public int getConfigVersion() {
        return configVersion;
    }

    public boolean isNorthboundActivated() {
        return northboundActivated;
    }

    public boolean isSouthboundActivated() {
        return southboundActivated;
    }

    public boolean isSkipVerification() {
        return skipVerification;
    }

    public @NotNull Map<String, Object> getAdapterConfiguration() {
        return adapterConfiguration;
    }

    public @NotNull RetryPolicyEntity getRetryPolicy() {
        return retryPolicy;
    }

    public long getWatchdogTimeoutMillis() {
        return watchdogTimeoutMillis;
    }

    public long getCommandTimeoutMillis() {
        return commandTimeoutMillis;
    }

    public @NotNull List<TagEntity> getTags() {
        return tags;
    }

    public @NotNull List<NorthboundMappingEntity> getNorthboundMappings() {
        return northboundMappings;
    }

    public @NotNull List<SouthboundMappingEntity> getSouthboundMappings() {
        return southboundMappings;
    }

    /**
     * {@code used} derivation: a tag is read-used iff at least one northbound mapping references it. This
     * is a pure scan of this adapter's mappings; it is computed on every {@code ConfigurationChanged} and shipped to
     * the wrapper inside {@code UpdateTagSet}.
     *
     * @return the names of the tags referenced by a northbound mapping, in first-seen order.
     */
    @JsonIgnore
    public @NotNull Set<String> getReadUsedTagNames() {
        return northboundMappings.stream()
                .map(NorthboundMappingEntity::getTagName)
                .collect(Collectors.toCollection(LinkedHashSet::new));
    }

    /**
     * {@code used} derivation: a tag is write-used iff at least one southbound mapping references it.
     *
     * @return the names of the tags referenced by a southbound mapping, in first-seen order.
     */
    @JsonIgnore
    public @NotNull Set<String> getWriteUsedTagNames() {
        return southboundMappings.stream()
                .map(SouthboundMappingEntity::getTagName)
                .collect(Collectors.toCollection(LinkedHashSet::new));
    }

    @JsonIgnore
    public boolean isReadUsed(final @NotNull String tagName) {
        return northboundMappings.stream()
                .anyMatch(mapping -> mapping.getTagName().equals(tagName));
    }

    @JsonIgnore
    public boolean isWriteUsed(final @NotNull String tagName) {
        return southboundMappings.stream()
                .anyMatch(mapping -> mapping.getTagName().equals(tagName));
    }

    @Override
    public void validate(final @NotNull List<ValidationEvent> validationEvents) {
        EntityValidatable.notEmpty(validationEvents, adapterId, "adapter-id");
        if (EntityValidatable.notEmpty(validationEvents, protocolId, "protocol-id")) {
            EntityValidatable.notMatch(
                    validationEvents,
                    () -> PROTOCOL_ID_PATTERN.matcher(protocolId).matches(),
                    () -> "protocol-id [" + protocolId + "] is not a valid identifier");
        }

        // S32: the watchdog must outlast the PA's command timeout, or every slow-but-normal command is misclassified
        // as a protocol violation.
        EntityValidatable.notMatch(
                validationEvents,
                () -> commandTimeoutMillis > 0,
                () -> "adapter [" + adapterId + "] command-timeout-millis (" + commandTimeoutMillis
                        + ") must be positive");
        EntityValidatable.notMatch(
                validationEvents,
                () -> watchdogTimeoutMillis > commandTimeoutMillis,
                () -> "adapter ["
                        + adapterId
                        + "] watchdog-timeout-millis ("
                        + watchdogTimeoutMillis
                        + ") must be strictly greater than command-timeout-millis ("
                        + commandTimeoutMillis
                        + ")");

        retryPolicy.validate(validationEvents);
        tags.forEach(tag -> tag.validate(validationEvents));
        getDuplicatedTagNameSet()
                .forEach(duplicate -> validationEvents.add(new ValidationEventImpl(
                        ValidationEvent.FATAL_ERROR,
                        "adapter [" + adapterId + "] declares tag name [" + duplicate + "] more than once",
                        null)));

        final Set<String> declaredTagNames =
                tags.stream().map(TagEntity::getName).collect(Collectors.toSet());
        northboundMappings.forEach(mapping -> mapping.validate(validationEvents));
        northboundMappings.forEach(mapping -> EntityValidatable.notMatch(
                validationEvents,
                () -> declaredTagNames.contains(mapping.getTagName()),
                () -> "northbound mapping references tag name ["
                        + mapping.getTagName()
                        + "] which is not declared in adapter ["
                        + adapterId
                        + "]"));
        southboundMappings.forEach(mapping -> mapping.validate(validationEvents));
        southboundMappings.forEach(mapping -> EntityValidatable.notMatch(
                validationEvents,
                () -> declaredTagNames.contains(mapping.getTagName()),
                () -> "southbound mapping references tag name ["
                        + mapping.getTagName()
                        + "] which is not declared in adapter ["
                        + adapterId
                        + "]"));
    }

    /**
     * @return the tag names this adapter declares more than once, in first-seen order; empty when there are none.
     */
    @JsonIgnore
    public @NotNull Set<String> getDuplicatedTagNameSet() {
        final Set<String> seen = new HashSet<>();
        final Set<String> duplicates = new LinkedHashSet<>();
        tags.stream().map(TagEntity::getName).forEach(tagName -> {
            if (!seen.add(tagName)) {
                duplicates.add(tagName);
            }
        });
        return duplicates;
    }

    @Override
    public boolean equals(final @Nullable Object o) {
        if (o instanceof final ProtocolAdapterEntity that) {
            return configVersion == that.configVersion
                    && northboundActivated == that.northboundActivated
                    && southboundActivated == that.southboundActivated
                    && skipVerification == that.skipVerification
                    && watchdogTimeoutMillis == that.watchdogTimeoutMillis
                    && commandTimeoutMillis == that.commandTimeoutMillis
                    && Objects.equals(adapterId, that.adapterId)
                    && Objects.equals(protocolId, that.protocolId)
                    && Objects.equals(adapterConfiguration, that.adapterConfiguration)
                    && Objects.equals(retryPolicy, that.retryPolicy)
                    && Objects.equals(tags, that.tags)
                    && Objects.equals(northboundMappings, that.northboundMappings)
                    && Objects.equals(southboundMappings, that.southboundMappings);
        }
        return false;
    }

    @Override
    public int hashCode() {
        return Objects.hash(
                adapterId,
                protocolId,
                configVersion,
                northboundActivated,
                southboundActivated,
                skipVerification,
                adapterConfiguration,
                retryPolicy,
                watchdogTimeoutMillis,
                commandTimeoutMillis,
                tags,
                northboundMappings,
                southboundMappings);
    }
}
